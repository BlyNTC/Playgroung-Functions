function splitSentence(string) {
  let result = [];
  let indexResult = 0;
  for (let indexString in string) {
    if (string[indexString] !== ' ') {
      if (result[indexResult] === undefined) {
        result[indexResult] = string[indexString];
      } else {
        result[indexResult] += string[indexString];
      }
    } else {
      indexResult += 1;
    }
  }
  return result;
}

