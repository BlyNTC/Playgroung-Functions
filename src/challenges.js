function highestCount(numArray) {
  let highest = -Infinity;
  let counts = {};
  for (const number of numArray) {
    if (number > highest) {
      highest = number;
    }
    if (counts[number]) {
      counts[number] += 1;
    } else {
      counts[number] = 1;
    }
  }
  return counts[highest];
}

